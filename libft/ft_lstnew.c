#include "libft.h"
#include <stdlib.h>

t_list	*ft_lstnew(void const *content, size_t content_size)
{
	t_list	*tmp;

	tmp = malloc(sizeof(t_list));
	if (tmp == NULL)
		return (NULL);
	if (content == NULL)
	{
		tmp->content = NULL;
		tmp->content_size = 0;
		tmp->next = NULL;
		return (tmp);
	}
	tmp->content = malloc(content_size);
	tmp->content_size = content_size;
	if (tmp->content == NULL)
		return (NULL);
	ft_memcpy(tmp->content, content, content_size);
	tmp->next = NULL;
	return (tmp);
}
