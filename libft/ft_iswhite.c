#include "libft.h"

int	ft_iswhite(char *str)
{
	int i;

	i = 0;
	while (str[i] && (str[i] == ' ' || str[i] == '\r'
				|| str[i] == '\t' || str[i] == '\n'))
		i++;
	return (i);
}
