#include "libft.h"

size_t	ft_strlcat(char *restrict dst, const char *restrict src, size_t size)
{
	size_t	i;
	size_t	j;
	size_t	srclen;

	i = 0;
	j = 0;
	srclen = ft_strlen(src);
	while (dst[i] && i < size)
		i++;
	if (size > 0)
	{
		while (src[j] && i < size - 1)
		{
			dst[i++] = src[j++];
		}
	}
	if (j > 0)
	{
		dst[i] = '\0';
		return (i + srclen - j);
	}
	return (srclen + i);
}
