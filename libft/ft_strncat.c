#include "libft.h"

char	*ft_strncat(char *restrict s1, const char *restrict s2, size_t n)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (s1[i])
	{
		i++;
	}
	while (s2[j] && (n - j) > 0)
	{
		s1[i + j] = s2[j];
		j++;
	}
	s1[i + j] = 0;
	return ((char*)s1);
}
