#include "libft.h"

int	ft_countdigits(int n)
{
	int		c;

	c = 0;
	if (n == 0)
		c++;
	else if (n < 0)
		c++;
	while (n != 0)
	{
		n = n / 10;
		c++;
	}
	return (c);
}
